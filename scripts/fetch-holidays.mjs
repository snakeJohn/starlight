#!/usr/bin/env node
// 构建前下载中国法定节假日数据,源:NateScarlet/holiday-cn (MIT)
// 覆盖当前年和下一年,多镜像 fallback。失败时不阻塞 build:
// 当前年下载失败但本地已有旧文件 → 保留旧文件;下一年未发布 → 跳过。
// 写入 src/data/holidays/{year}.json 并重新生成 index.ts。
//
// 由 package.json 的 "prebuild" 钩子触发,npm run build 时自动执行。

import { mkdirSync, writeFileSync, existsSync, readdirSync } from 'node:fs';
import { join, dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const DATA_DIR = join(ROOT, 'src', 'data', 'holidays');

const MIRRORS = [
  (year) => `https://cdn.jsdelivr.net/gh/NateScarlet/holiday-cn@master/${year}.json`,
  (year) => `https://fastly.jsdelivr.net/gh/NateScarlet/holiday-cn@master/${year}.json`,
  (year) => `https://raw.githubusercontent.com/NateScarlet/holiday-cn/master/${year}.json`,
];

async function downloadYear(year) {
  for (const buildUrl of MIRRORS) {
    const url = buildUrl(year);
    try {
      const ac = new AbortController();
      const timer = setTimeout(() => ac.abort(), 15000);
      const res = await fetch(url, { signal: ac.signal });
      clearTimeout(timer);
      if (res.status === 404) {
        console.log(`  ℹ ${year}: ${url} → 404 (尚未发布)`);
        return { status: 'not-found' };
      }
      if (!res.ok) {
        console.log(`  ⚠ ${year}: ${url} → HTTP ${res.status},换下一个镜像`);
        continue;
      }
      const json = await res.json();
      if (!json || !Array.isArray(json.days)) {
        console.log(`  ⚠ ${year}: ${url} → 响应格式异常 (无 days[]),换下一个镜像`);
        continue;
      }
      for (const d of json.days) {
        if (typeof d.date !== 'string' || typeof d.isOffDay !== 'boolean') {
          console.log(`  ⚠ ${year}: 字段不全 (${JSON.stringify(d)}),换下一个镜像`);
          continue;
        }
      }
      console.log(`  ✓ ${year}: ${url} (${json.days.length} 条)`);
      return { status: 'ok', data: json };
    } catch (e) {
      console.log(`  ⚠ ${year}: ${url} → ${e.message || e},换下一个镜像`);
    }
  }
  return { status: 'failed' };
}

function writeYear(year, data) {
  mkdirSync(DATA_DIR, { recursive: true });
  const file = join(DATA_DIR, `${year}.json`);
  writeFileSync(file, JSON.stringify(data, null, 2) + '\n');
}

function regenerateIndex() {
  const files = readdirSync(DATA_DIR)
    .filter((f) => /^\d{4}\.json$/.test(f))
    .sort();
  const lines = [
    '// AUTO-GENERATED by scripts/fetch-holidays.mjs — DO NOT EDIT',
    '// 由 prebuild 钩子在构建时刷新。手工改动会被覆盖。',
    '',
    'import type { HolidayYear } from \'../../utils/holiday\';',
    '',
  ];
  const idents = [];
  for (const f of files) {
    const year = f.replace(/\.json$/, '');
    const ident = `y${year}`;
    idents.push(ident);
    lines.push(`import ${ident} from './${f}';`);
  }
  lines.push('');
  lines.push(`export const ALL_HOLIDAYS: HolidayYear[] = [${idents.join(', ')}];`);
  lines.push('');
  writeFileSync(join(DATA_DIR, 'index.ts'), lines.join('\n'));
  console.log(`  ✓ 已生成 src/data/holidays/index.ts (含 ${files.length} 个年份: ${files.map(f => f.replace('.json','')).join(', ')})`);
}

async function main() {
  const now = new Date();
  const currentYear = now.getFullYear();
  const nextYear = currentYear + 1;

  console.log(`[fetch-holidays] 目标年份: ${currentYear}, ${nextYear}`);
  mkdirSync(DATA_DIR, { recursive: true });

  const results = await Promise.all([
    downloadYear(currentYear),
    downloadYear(nextYear),
  ]);

  const [curRes, nextRes] = results;

  if (curRes.status === 'ok') {
    writeYear(currentYear, curRes.data);
  } else if (existsSync(join(DATA_DIR, `${currentYear}.json`))) {
    console.log(`  ℹ ${currentYear}: 下载失败但本地已有快照,沿用旧文件`);
  } else {
    console.log(`  ⚠ ${currentYear}: 下载失败且无本地快照,节假日功能将退化(查询返回 undefined)`);
  }

  if (nextRes.status === 'ok') {
    writeYear(nextYear, nextRes.data);
  } else if (nextRes.status === 'not-found') {
    // 跳过,正常情况(每年 11 月前国务院尚未发布次年安排)
  } else if (existsSync(join(DATA_DIR, `${nextYear}.json`))) {
    console.log(`  ℹ ${nextYear}: 下载失败但本地已有快照,沿用旧文件`);
  }

  regenerateIndex();
  console.log('[fetch-holidays] 完成');
}

main().catch((e) => {
  // 主流程异常仍以 0 退出,避免阻塞构建
  console.error('[fetch-holidays] 发生异常:', e);
  console.error('[fetch-holidays] 节假日数据未更新,但构建继续');
});
